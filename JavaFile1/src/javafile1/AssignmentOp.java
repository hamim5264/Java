public class AssignmentOp {
    public static void main (String[] args){
        int x = 3;
        int y = 4;
        x+=y;
        System.out.println("X="+x);
        x-=y;
        System.out.println("X="+x);
        x*=y;
        System.out.println("X="+x);
        x/=y;
        System.out.println("X="+x);
        x%=y;
        System.out.println("X="+x);
    }
    
}
